package basic;

public class ForLoop
{
    public static void main(String[] args)
    {
        for(int i = 1; i <= 10; i++)
        {
            System.out.println(i + " Bangladesh.");
        }
        for(int j = 2; j <= 100; j += 2)
        {
            System.out.println(j);
        }
    }
}
