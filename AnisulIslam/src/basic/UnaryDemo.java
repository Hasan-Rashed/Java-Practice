package basic;

public class UnaryDemo
{
    public static void main(String[] args)
    {
        int x = -10, result;

        result = +x; // Unary plus(+) works with only one operand
        System.out.println("result: " + result);

        result = -x; // Unary minus(+) works with only one operand
        System.out.println("result: " + result);
    }
}
