package basic;

public class WhileLoop
{
    public static void main(String[] args)
    {
        int i = 1, j = 2;
        while(i <= 10)
        {
            System.out.println(i + " Bangladesh.");
            i++;
        }

        while(j <= 100)
        {
            System.out.println(j);
            j += 2;
        }
    }
}
